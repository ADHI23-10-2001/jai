class Swapping2
{
 public static void main(String args[])
 {
  int C=30,D=20;
	System.out.println("Before swapping C value:"+C);
	System.out.println("Before swapping D value:"+D);
   C=C+D;
   D=C-D;
   C=C-D;
	System.out.println("After swapping C value:"+C);
	System.out.println("After swapping D value:"+D);
 }
}
   
